
public class Bonny {
	private long act = 0;
	private long pos = 1;
	
	public Bonny(long act, long pos) {
		super();
		this.act = act;
		this.pos = pos;
	}
	public long getAct() {
		return act;
	}
	public void setAct(long act) {
		this.act = act;
	}
	public long getPos() {
		return pos;
	}
	public void setPos(long pos) {
		this.pos = pos;
	}
	
	
}
