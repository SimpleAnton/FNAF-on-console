
public class Foxy {
	private long act = 0;
	private long state = 3;
	
	
	public Foxy(long act, long state) {
		super();
		this.act = act;
		this.state = state;
	}
	public long getAct() {
		return act;
	}
	public void setAct(long act) {
		this.act = act;
	}
	public long getState() {
		return state;
	}
	public void setState(long state) {
		this.state = state;
	}
	
	
}
