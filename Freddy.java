
public class Freddy {
	public long act = 0;
	public long pos = 2;
	
	public Freddy(long act, long pos) {
		super();
		this.act = act;
		this.pos = pos;
	}
	public long getAct() {
		return act;
	}
	public void setAct(long act) {
		this.act = act;
	}
	public long getPos() {
		return pos;
	}
	public void setPos(long pos) {
		this.pos = pos;
	}
	
	
}
